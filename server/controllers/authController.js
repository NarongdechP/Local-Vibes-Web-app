export const register = async (req, res) => {
    const errors = validationResult(req);
    if (!errors.isEmpty()) {
        return res.status(400).json({ errors: errors.array() });
    }

    let { username, email, password, profile_pic } = req.body;

    username = sanitizeHtml(username);
    email = sanitizeHtml(email);
    profile_pic = profile_pic ? sanitizeHtml(profile_pic) : null;

    try {
        const existingEmail = await User.findOne({ email });
        if (existingEmail) return res.status(400).json({ error: "‡∏≠‡∏µ‡πÄ‡∏°‡∏•‡∏ô‡∏µ‡πâ‡∏ñ‡∏π‡∏Å‡πÉ‡∏ä‡πâ‡πÑ‡∏õ‡πÅ‡∏•‡πâ‡∏ß" });

        const existingUsername = await User.findOne({ username });
        if (existingUsername) return res.status(400).json({ error: "‡∏ä‡∏∑‡πà‡∏≠‡∏ú‡∏π‡πâ‡πÉ‡∏ä‡πâ‡∏ô‡∏µ‡πâ‡∏ñ‡∏π‡∏Å‡πÉ‡∏ä‡πâ‡πÑ‡∏õ‡πÅ‡∏•‡πâ‡∏ß" });

        const hashedPassword = await bcrypt.hash(password, 10);

        const newUser = new User({ username, email, password: hashedPassword, profile_pic });
        await newUser.save();

        res.status(201).json({ message: "‡∏™‡∏°‡∏±‡∏Ñ‡∏£‡∏™‡∏°‡∏≤‡∏ä‡∏¥‡∏Å‡∏™‡∏≥‡πÄ‡∏£‡πá‡∏à", user: newUser });
    } catch (err) {
        console.error("üî¥ Register Error:", err);
        res.status(500).json({ error: "Internal Server Error" });
    }
};

